    private void cadastrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarButtonActionPerformed
        // TODO add your handling code here:

        String[] checkStrings = new String[5];

        checkStrings[0] = nomeCompletoTextField.getText();
        checkStrings[1] = descricaoTextArea.getText();
        checkStrings[2] = radioButtonSelected;
        checkStrings[3] = tipoComboBox.getSelectedItem().toString();
        checkStrings[4] = valorVendaTextField.getText();

        if (!hasNullFields(checkStrings)) {

            try {

                double valorVenda = Double.parseDouble(checkStrings[4]);
                String valorVendaFormatted = NumberFormat.getCurrencyInstance().format(valorVenda);

                checkStrings[4] = valorVendaFormatted;

                Imovel imovel = new Imovel(
                        checkStrings[0],
                        checkStrings[1],
                        checkStrings[2],
                        checkStrings[3],
                        checkStrings[4]);

                imoveis.add(imovel);

                JOptionPane.showMessageDialog(rootPane, "Cadastro efetuado com sucesso");
                resetAllFields();
            } catch (NumberFormatException e) {

                JOptionPane.showMessageDialog(rootPane, "Valor da venda inválido: utilize somente números");
            }

        } else {

            JOptionPane.showMessageDialog(rootPane, "Todos os campos devem ser preenchidos");
        }
    }//GEN-LAST:event_cadastrarButtonActionPerformed

