    private void excluirCadastroMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirCadastroMenuItemActionPerformed
        // TODO add your handling code here:

        String numeroCadastroString = JOptionPane.showInputDialog(rootPane, "Informe o número do cadastro (posição do cadastro na lista):");

        if (numeroCadastroString.matches("^[0-9][1-9]*$")) {
            int numeroCadastro = Integer.parseInt(numeroCadastroString);

            if (numeroCadastro < imoveis.size()) {
                imoveis.remove(numeroCadastro);
                JOptionPane.showMessageDialog(rootPane, "O Cadastro informado foi deletado com sucesso");
            } else {
                JOptionPane.showMessageDialog(rootPane, "O Cadastro informado não existe");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Utilize somente números");
        }
    }//GEN-LAST:event_excluirCadastroMenuItemActionPerformed

    private void excluirTodosCadastrosMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirTodosCadastrosMenuItemActionPerformed
        // TODO add your handling code here:
        if (!imoveis.isEmpty()) {
            imoveis.clear();
            JOptionPane.showMessageDialog(rootPane, "Todos os cadastros foram deletados com sucesso");
        } else {
            
            JOptionPane.showMessageDialog(rootPane, "Não há nenhum cadastro existente para ser deletado");
        }
    }//GEN-LAST:event_excluirTodosCadastrosMenuItemActionPerformed

    private void resetAllFields() {
        nomeCompletoTextField.setText("");
        descricaoTextArea.setText("");
        buttonGroup.clearSelection();
        tipoComboBox.setSelectedIndex(0);
        valorVendaTextField.setText("");
    }

    private boolean hasNullFields(String[] fields) {

        for (int i = 0; i < fields.length; i++) {

            if (fields[i].isEmpty()) {
                return true;
            }
        }

        return false;
    }

